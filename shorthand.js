// Variable set
var rice = 250;
// variable update
rice = 54;

// Add something to the existing value
// rice = rice + 20;
rice += 20;
console.log(rice);

// Deducting something to existing value
rice -= 5;
console.log(rice);

// multiply something to existing value
rice *= 5;
console.log(rice);

//Divide something to existing value
rice /= 3;
console.log(rice);

// add just 1 to the existing value
rice++;
console.log(rice);
// Deduct just 1 to the existing value
rice--;
console.log(rice);
